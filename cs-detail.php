	  <h3>Safeguarding project (12-2005/09-2007)</h3>
		<p>The Zápara communities in the Amazon have developed a complex oral culture expressed through their myths, rituals, artistic and medical practices. Their traditions are deeply marked by their environment and reflect a profound knowledge of the Amazonian jungle.
The project aimed at facilitaing the revival of the Zápara language and to encourage binational encounters in the hope of strengthening the organization of the Zápara community in Ecuador and Peru. The project also intended to ensure continued transmission of oral and other traditions and introduce such transmission in areas where native speakers no longer exist.
A census both in Ecuador and Peru was conducted in order to determine the number and exact location of all the members of the Zápara community and the degree of conservation of the Zápara language. Various meetings were organized in order to bring together the Zápara people in both countries, allowing them to meet one another and forge links.</p>
