		<h2>Oral heritage and cultural manifestations of the Zápara people</h2>
		<h3>Description</h3>
		<p>The Zápara people of the Amazon rainforest are inhabitants of one of the most bio-diverse areas of the world. They have elaborated an oral culture that is particularly rich as regards their understanding of the natural environment. This is demonstrated by the abundance of their vocabulary for the flora and fauna and by their medicinal practices and knowledge of the medicinal plants of the forest. In 2008 it has been inscribed on the Representative List of the Intangible Cultural Heritage of Humanity (originally proclaimed in 2001). </p>
		<p>Four centuries of history, marked by the Spanish conquest, slavery, epidemics, forced conversions, wars and deforestation, have driven the Zápara people to near extinction. Despite these numerous threats, they have managed to preserve their ancestral knowledge. Intermarriage with Mestizos and other indigenous peoples (Quechua) has been especially instrumental in enabling the people to survive. But this dispersion has also resulted in a partial loss of their identity. </p>
		<p>The current situation of the Zápara people is critical, and today they are in very serious danger of disappearing altogether. In 2001, their population numbered no more than 300 (200 in Ecuador and 100 in Peru), of whom only five, all aged over 70, still speak the Zápara language.</p>
